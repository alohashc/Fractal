#include "libft.h"

void	ft_putnbr(int nb)
{
	int flag;

	flag = 0;
	if (nb == -2147483648)
	{
		nb = nb / 10;
		flag = -1;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10 && nb <= 2147483647)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	if (nb >= 0 && nb <= 9)
	{
		nb = nb + '0';
		write(1, &nb, 1);
	}
	if (flag < 0)
		write(1, "8", 1);
}
